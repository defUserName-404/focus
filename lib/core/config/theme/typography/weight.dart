/// Font weight constants for variable font (Manrope)
///
/// Standard font weight values from 100 (thin) to 900 (black).
/// Used with [FontVariation] for dynamic weight control.
final class _AppTypographyWeight {
  const _AppTypographyWeight();

  final thin = 100.0;
  final extralight = 200.0;
  final light = 300.0;
  final normal = 400.0;
  final medium = 500.0;
  final semibold = 600.0;
  final bold = 700.0;
  final extrabold = 800.0;
  final black = 900.0;
}
